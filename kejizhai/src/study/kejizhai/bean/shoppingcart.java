public class shoppingcart {
    private int scid;
    private int uid;
    private int iid;
    private int count;
    private int price;

public shoppingcart(int scid, int uid, int iid, int count, int price) { 
    this.scid = scid;
    this.uid = uid;
    this.iid = iid;
    this.count = count;
    this.price = price;
}
}